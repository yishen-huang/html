function openModal() {
    var modal = document.getElementById('recipeModal');
    modal.style.display = 'block';
    setTimeout(function() {
        modal.style.display = 'none';
    }, 5000);
}

function openHamburgerModal() {
    var modal = document.getElementById('hamburgerModal');
    modal.style.display = 'block';
    setTimeout(function() {
        modal.style.display = 'none';
    }, 5000);
}